package curso_loiane.aulas.aula11;

public class CuriosidadeInteiros {
    public static void main(String[] args) {

        int var1 = 2147483647;
        int var2 = 1;

        System.out.println(var1 + var2);

        // maior valor de int: 2147483647
        // menor valor de int: -2147483548
        // Funciona como uma roleta, quando atinge o maior número, não dá erro, ele passa para o menor número
    }
}
